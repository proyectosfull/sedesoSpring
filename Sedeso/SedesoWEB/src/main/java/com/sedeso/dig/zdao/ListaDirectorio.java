package com.sedeso.dig.zdao;

import java.util.List;

public class ListaDirectorio {
	
	public String nombreDir;
	public List<String> archivoDir;
	
	public String getNombreDir() {
		return nombreDir;
	}
	public void setNombreDir(String nombreDir) {
		this.nombreDir = nombreDir;
	}
	public List<String> getArchivoDir() {
		return archivoDir;
	}
	public void setArchivoDir(List<String> archivoDir) {
		this.archivoDir = archivoDir;
	}
	
	
}
